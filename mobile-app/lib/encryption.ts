import CryptoJS from 'crypto-js';
import Constants from 'expo-constants';

const configuredSalt = process.env.EXPO_PUBLIC_ENCRYPTION_SALT || Constants.expoConfig?.extra?.encryptionSalt;
if (!configuredSalt && process.env.NODE_ENV === 'production') {
    throw new Error('EXPO_PUBLIC_ENCRYPTION_SALT is required in production');
}
if (!configuredSalt && process.env.NODE_ENV !== 'production') {
    console.warn('[Encryption] EXPO_PUBLIC_ENCRYPTION_SALT is not set. Using development fallback salt.');
}
const GLOBAL_SALT = configuredSalt || 'dev-only-fallback-salt';

/**
 * Generates a deterministic shared key for a chat between two users.
 * Ensure the IDs are sorted so User A + User B generates the same key as User B + User A.
 */
export function generateChatKey(userId1: string, userId2: string): string {
    if (!userId1 || !userId2) throw new Error("Both user IDs are required to generate a chat key");
    
    // Sort the IDs alphabetically so the order doesn't matter
    const sortedIds = [userId1, userId2].sort().join(':');
    
    // Hash the combined string along with the secret global salt
    return CryptoJS.SHA256(`${sortedIds}-${GLOBAL_SALT}`).toString(CryptoJS.enc.Hex);
}

/**
 * Encrypts a plaintext message using the shared chat key.
 * @param plaintext The message to encrypt
 * @param sharedKey The chat key generated by `generateChatKey`
 * @returns The encrypted ciphertext string
 */
export function encryptMessage(plaintext: string, sharedKey: string): string {
    if (!plaintext) return '';
    return CryptoJS.AES.encrypt(plaintext, sharedKey).toString();
}

/**
 * Decrypts a ciphertext string back into plaintext.
 * @param ciphertext The encrypted message
 * @param sharedKey The chat key generated by `generateChatKey`
 * @returns The decrypted plaintext, or a fallback error string if decryption fails
 */
export function decryptMessage(ciphertext: string, sharedKey: string): string {
    if (!ciphertext) return '';
    try {
        const bytes = CryptoJS.AES.decrypt(ciphertext, sharedKey);
        // If decryption produces empty/invalid UTF-8, fall back to raw text
        // (this handles old plain-text messages stored before encryption was added)
        const decryptedText = bytes.toString(CryptoJS.enc.Utf8);
        if (!decryptedText) return ciphertext; // Return original if decryption yields nothing
        return decryptedText;
    } catch (error) {
        // Malformed UTF-8 or invalid ciphertext — treat as plain text
        return ciphertext;
    }
}
